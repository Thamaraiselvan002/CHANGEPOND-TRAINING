package car;

public class Car {
	public String audi;
	public String bmw;
	public String maruti;
	
	public String getAudi() {
		return audi;
	}
	public void setAudi(String audi) {
		this.audi = audi;
	}
	public String getBmw() {
		return bmw;
	}
	public void setBmw(String bmw) {
		this.bmw = bmw;
	}
	public String getMaruti() {
		return maruti;
	}
	public void setMaruti(String maruti) {
		this.maruti = maruti;
	}

	
}



